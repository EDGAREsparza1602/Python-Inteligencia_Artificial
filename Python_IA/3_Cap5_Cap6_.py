# Se utiliza 'title' se utiliza para convertir la primera letra en mayuscula
# Se utiliza '\n' para realizar saltos de linea

nombre = "\nedgar alejandro esparza palencia\n".title()

print(nombre)

# Se utiliza 'lower' para convertir toda la cadena en minusculas
 
nombre1 = "edgar alejandro esparza palencia\n".lower()
print(nombre1)

# Se utiliza 'upper' para convertir toda la cadena  en mayusculas

nombre2 = "edgar alejandro esparza palencia\n".upper()
print(nombre2)

